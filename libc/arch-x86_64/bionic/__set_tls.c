#include <pthread.h>
#include <asm/prctl.h>
#include <sys/prctl.h>

/*
 * This is much simple for x86_64 than x86. arch_prctl is x86_64 specific
 * syscall which does everything we need.
 */
int __set_tls(void *ptr)
{
    /* We also need to write the location of the tls to ptr[0] */
    *(void **)ptr = ptr;

    return arch_prctl(ARCH_SET_FS, ptr);
}
